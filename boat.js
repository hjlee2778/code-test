function solution(people, limit) {
    // 사람들의 몸무게를 오름차순으로 정렬
    people.sort((a, b) => a - b);
    
    let boats = 0; // 필요한 구명보트의 개수
    let left = 0; // 가장 가벼운 사람의 인덱스
    let right = people.length - 1; // 가장 무거운 사람의 인덱스

    // 모든 사람을 보트에 태울 때까지 반복
    while (left <= right) {
        // 최대한 보트를 적게 쓰려면 짝지어서 보내야 함
        //  가장 가벼운 사람과 가장 무거운 사람의 무게 합이 제한보다 작거나 같으면 둘을 같이 태움.

        if (people[left] + people[right] <= limit) {
            left++; // 가장 가벼운 사람을 태웠으므로 다음 가벼운 사람을 가리킴
        }

        // 가장 무거운 사람은 보트에 태움.
        // 가장 무거운 사람을 선택하는 이유는 가장 무거운 사람은 짝지을 수 없을 확률이 높다
        // 따라서 최소한으로 보트를 쓰기 위해서는 최대한 많이 짝지어야 함
        right--; // 가장 무거운 사람을 태웠으므로 다음 무거운 사람을 가리킵니다.
        
        // 보트 하나를 사용했으므로, 보트 개수를 증가시킴.
        boats++;
    }

    // 필요한 보트의 개수를 반환
    return boats;
}